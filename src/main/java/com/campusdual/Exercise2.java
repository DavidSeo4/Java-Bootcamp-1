package com.campusdual;

public class Exercise2 {

    //radio 15

    //area circulo, longitud circunferencia

    public static void main(String[] args) {

        double radio = 15.0;

        double area_circulo = Math.PI * Math.pow(radio, 2);

        System.out.println("Area circulo: " + area_circulo);

        double longitudCircunferencia = 2 * Math.PI * radio;

        System.out.println("Longitud circunferencia: " + longitudCircunferencia);


    }





}
